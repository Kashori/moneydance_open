package com.moneydance.modules.features.debtinsights.model;


public class InvalidWrapperException extends Exception
{
	
	public InvalidWrapperException()
	{
		super();
	}
	
	public InvalidWrapperException(String arg0)
	{
		super(arg0);
	}
	
	public InvalidWrapperException(Throwable arg0)
	{
		super(arg0);
	}
	
	public InvalidWrapperException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}
	
}
